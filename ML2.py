# รายการ-list,รวบรวมข้อมูล-list

a = list([1,2,3,4,5,6])  # ลิสต์ที่มีสมาชิก 5 ตัวคือ 1,2,3,4,5,6
b = list(range(3,6))     # สร้างลิสต์โดยใช้ range() ซึ่งมีสมาชิกเป็น 3,4,5
c = list([1,True,'Three',4.5])  # สมาชิกของลิสต์อาจเป็นข้อต่างชนิดกันก็ได้
d = list('abcd')                # ลิสต์ที่สมาชิก 4 ตัวคือ 'a','b'.'c','d'
